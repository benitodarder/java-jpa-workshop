-- Schema
CREATE TABLE MASTER (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
	name varchar(64),
	enabled BOOLEAN DEFAULT TRUE NOT NULL,
	value01 DOUBLE,
	value02 VARCHAR(64),
	value03 DOUBLE
)
CREATE TABLE SECONDARY (
	id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
	name varchar(64),
	enabled BOOLEAN DEFAULT TRUE NOT NULL,
	value02 VARCHAR(64),
	value03 DOUBLE,
	masterId INT,
	FOREIGN KEY(masterId) REFERENCES MASTER(id) 
)
-- Hierarchical Schema
CREATE SEQUENCE seq_base_Id START WITH 1;
-- Sequence must be created and commited before the creation of tables
CREATE TABLE BASE (
	id INT GENERATED BY DEFAULT AS SEQUENCE seq_base_Id PRIMARY KEY, 
	name varchar(64),
	enabled BOOLEAN DEFAULT TRUE NOT NULL,
	commonValue varchar(64)
)
CREATE TABLE MASTER_FROM_BASE (
	id INT PRIMARY KEY, 
	name varchar(64),
	enabled BOOLEAN DEFAULT TRUE NOT NULL,
	value01 DOUBLE,
	value02 VARCHAR(64),
	value03 DOUBLE,
	FOREIGN KEY(id) REFERENCES BASE(id) 
)
CREATE TABLE SECONDARY_FROM_BASE (
	id INT  PRIMARY KEY, 
	name varchar(64),
	enabled BOOLEAN DEFAULT TRUE NOT NULL,
	value02 VARCHAR(64),
	value03 DOUBLE,
	masterId INT,
	FOREIGN KEY(id) REFERENCES BASE(id), 
	FOREIGN KEY(masterId) REFERENCES MASTER_FROM_BASE(id) 
);
